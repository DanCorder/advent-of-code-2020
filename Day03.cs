        public static int SolveProblem1()
        {
            var rows = ProblemInput.SplitToLines().ToList();
            var trees = 0;
            var rowLength = rows.First().Length;
            var xPos = 0;

            for (var i = 1; i < rows.Count; i++)
            {
                xPos = (xPos + 3) % rowLength;
                if (rows[i][xPos] == '#')
                {
                    trees++;
                }
            }
            return trees;
        }

        public static long SolveProblem2()
        {
            var rows = ProblemInput.SplitToLines().ToList();
            long trees1 = slopeCount(rows, 1, 1);
            long trees2 = slopeCount(rows, 3, 1);
            long trees3 = slopeCount(rows, 5, 1);
            long trees4 = slopeCount(rows, 7, 1);
            long trees5 = slopeCount(rows, 1, 2);

            return trees1 * trees2 * trees3 * trees4 * trees5;
        }

        private static long slopeCount(List<string> map, int x, int y)
        {
            var xPos = 0;
            var trees = 0;
            var rowLength = map.First().Length;
            for (var i = y; i < map.Count; i += y)
            {
                xPos = (xPos + x) % rowLength;
                if (map[i][xPos] == '#')
                {
                    trees++;
                }
            }
            return trees;
        }
